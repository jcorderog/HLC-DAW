<?php 
    //Con print puedes imprimir cadenas separadas por puntos.
    print "hola con print\n"."hola\n";
    //Al igual que con print, print() puede imprimir cadenas separadas por puntos.
    print("hola con print()\n"."hola\n");
    /*Con print_r  podemos mostrar cadenas o valores separados por puntos, también podemos mostrar arrays.*/
    print_r("hola con print_r()\n"."adios\n");
    /*Con echo podemos mostrar un conjunto de cadenas pero no podemos mostrar otros tipo de valores.*/
    echo "hola con echo", " Segunda linea de echo.\n";
?>